#Write program to Add, Subtract, Multiply, Divide 2 Complex numbers.

complex1 = eval(input("enter 1st complex number"))
complex2  = eval(input("enter 2nd complex number"))

#addition
add = complex1 + complex2
print("addition of two complex number", add)

#subtraction
sub = complex1 - complex2
print("subtraction of two complex number", sub)

#multiplication
mul = complex1 * complex2
print("multiplication of two complex number", mul)

#division
div = complex1 / complex2
print("division of two complex number", div)